#include <assert.h>

/* doubles */
double double_add (double a, double b) { return a + b; }
double double_sub (double a, double b) { return a - b; }
double double_mul (double a, double b) { return a * b; }
double double_div (double a, double b) { return a / b; }
double double_neg (double a) { return -a; }

int eq_double (double a, double b) { return a == b; }
int ne_double (double a, double b) { return a != b; }
int gt_double (double a, double b) { return a > b; }
int ge_double (double a, double b) { return a >= b; }
int lt_double (double a, double b) { return a < b; }
int le_double (double a, double b) { return a <= b; }

void Tdouble()
{
    /* do doubles */
    assert(double_add (1, 1) == 2);
    assert(double_sub (3, 2) == 1);
    assert(double_mul (2, 3) == 6);
    assert(double_div (3, 2) == 1.5);
    assert(double_neg (1) == -1);


    assert(eq_double (1, 1) != 0);
    assert(eq_double (1, 2) == 0);
    assert(ne_double (1, 2) != 0);
    assert(ne_double (1, 1) == 0);
    assert(gt_double (2, 1) != 0);
    assert(gt_double (1, 1) == 0);
    assert(gt_double (0, 1) == 0);
    assert(ge_double (2, 1) != 0);
    assert(ge_double (1, 1) != 0);
    assert(ge_double (0, 1) == 0);
    assert(lt_double (1, 2) != 0);
    assert(lt_double (1, 1) == 0);
    assert(lt_double (1, 0) == 0);
    assert(le_double (1, 2) != 0);
    assert(le_double (1, 1) != 0);
    assert(le_double (1, 0) == 0);


}
 
